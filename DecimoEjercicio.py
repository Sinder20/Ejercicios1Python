#  10. Recibir 2 numeros por teclado y mostrar la suma y la multiplicación de los mismos

print("* * * Programa que suma y multiplica dos números * * *")

num1= int(input("Ingrese el primer número: "))
num2= int(input("Ingrese el segundo número: "))

suma= num1+num2
multiplicacion= num1*num2

print("La suma de", num1, "más", num2, "es: ", suma)
print("La multiplicación de", num1, "por", num2, "es: ", multiplicacion)